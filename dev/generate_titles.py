#!/usr/bin/env python3
"""
Generate EU5/Etra fantasy title translations with game rule toggle.

Reads title translations from YAML config and generates:
  1. Game rule file (pre-game toggle: English / Fantasy)
  2. Customizable localization (dynamic per ruler culture)
  3. Localization YML (all translated names)

Output goes to overrides/ so generate_mod.py --all picks it up automatically.

Usage:
    python generate_titles.py                    # generate from titles/config.yaml
    python generate_titles.py titles/custom.yaml # use specific config
    python generate_titles.py --list             # show translation status
    python generate_titles.py --audit            # find English names needing translation

Requires: PyYAML (pip install pyyaml)
"""
