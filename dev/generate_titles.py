def list_titles(cfg: dict):
    """Print summary of all titles and translation status."""
    titles = cfg.get("titles", {})
    languages = cfg.get("languages", [])

    if not titles:
        print("No titles in config yet. Add entries to titles/config.yaml")
        return

    print(f"{'TAG':<8} {'English Name':<35} {'Native':<18} {'Translations'}")
    print("-" * 95)

    for tag, data in titles.items():
        if not isinstance(data, dict):
            continue
        english = data.get("english", "???")
        native = lang_short(data.get("native", "???"))
        translations = data.get("translations", {})
        langs = ", ".join(lang_short(l) for l in translations)
        print(f"{tag:<8} {english:<35} {native:<18} {langs}")

    n_titles = len(titles)
    n_translations = sum(
        len(d.get("translations", {}))
        for d in titles.values()
        if isinstance(d, dict)
    )
    n_languages = len(languages) if languages else 7
    coverage = (n_translations / (n_titles * n_languages) * 100) if n_titles else 0

    print(f"\nTotal: {n_titles} titles, {n_translations} translations")
    print(f"Coverage: {coverage:.0f}% ({n_translations}/{n_titles * n_languages})")


