def generate_localization(cfg: dict) -> str:
    """Generate localization .yml content with game rule labels + translations."""
    titles = cfg.get("titles", {})
    gr = cfg.get("game_rule", {})
    key = gr.get("key", "etra_title_names")
    desc_en = gr.get("desc_english", "Use English names for countries and titles")
    desc_fantasy = gr.get("desc_fantasy", "Use fantasy language names based on ruler's culture")
    label = gr.get("label", "Etra: Title Names")

    lines = [
        "# Etra: Names & Cultures — Title translation localization",
        "# Generated by generate_titles.py",
        "",
        "l_english:",
        "",
        " # --- GAME RULE ---",
        f' rule_{key}: "{label}"',
        f' setting_etra_names_english: "English"',
        f' setting_etra_names_english_desc: "{desc_en}"',
        f' setting_etra_names_fantasy: "Fantasy Languages"',
        f' setting_etra_names_fantasy_desc: "{desc_fantasy}"',
    ]

    if not titles:
        return "\n".join(lines) + "\n"

    lines.append("")
    lines.append(" # --- TITLE TRANSLATIONS ---")

    for tag, data in titles.items():
        if not isinstance(data, dict):
            continue
        translations = data.get("translations", {})
        english = data.get("english", tag)
        if not translations:
            continue

        lines.append(f" # {tag}: {english}")
        for lang_id, translated_name in translations.items():
            loc_key = f"{tag}_{lang_short(lang_id)}"
            # Escape quotes in names
            safe_name = translated_name.replace('"', '\\"')
            lines.append(f' {loc_key}: "{safe_name}"')

        # ADJ localization keys (for $ADJ$ $RANK$ construction)
        adjectives = data.get("adjectives", {})
        if adjectives:
            for lang_id, adj_name in adjectives.items():
                loc_key = f"{tag}_{lang_short(lang_id)}_ADJ"
                safe_adj = adj_name.replace('"', '\\"')
                lines.append(f' {loc_key}: "{safe_adj}"')
            # Native fallback ADJ
            native = data.get("native")
            if native and native in adjectives:
                native_adj_key = f"{tag}_{lang_short(native)}_ADJ"
                # Already added above, but we need the bare fallback key
                # TAG_ADJ fallback is handled by parent = TAG suffix = "_ADJ"
                # which appends _ADJ to the fallback loc key TAG → TAG_ADJ
                # TAG_ADJ is already defined by Etra mod as the English adjective

        lines.append("")

    return "\n".join(lines) + "\n"


