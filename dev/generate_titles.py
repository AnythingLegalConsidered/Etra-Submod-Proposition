def lang_short(lang_id: str) -> str:
    """Convert language_id to short form for loc keys."""
    return lang_id.replace("_language", "")


def generate_custom_loc(cfg: dict) -> str:
    """Generate customizable_localization .txt content.

    For each title with translations, creates a scope that resolves
    the country name based on game rule + ruler culture.

    Resolution order (first match wins):
      1. Fantasy mode + ruler's specific language → that language's name
      2. Fantasy mode + no match → native language name
      3. English fallback
    """
    titles = cfg.get("titles", {})
    if not titles:
        return ""

    lines = [
        "# Etra: Names & Cultures — Dynamic title names",
        "# Generated by generate_titles.py",
        "#",
        "# Resolves country names based on game rule + ruler culture.",
        "# When game rule = Fantasy: uses ruler's culture language.",
        "# When game rule = English: uses base English name.",
        "",
    ]

    for tag, data in titles.items():
        if not isinstance(data, dict):
            continue
        translations = data.get("translations", {})
        native = data.get("native")
        if not translations:
            continue

        english = data.get("english", tag)
        lines.append(f"# {tag}: {english}")
        lines.append(f"{tag} = {{")
        lines.append("\ttype = country")

        # 1. Per-language triggers (most specific)
        for lang_id in translations:
            loc_key = f"{tag}_{lang_short(lang_id)}"
            lines.append("\ttext = {")
            lines.append(f"\t\tlocalization_key = {loc_key}")
            lines.append("\t\ttrigger = {")
            lines.append("\t\t\thas_game_rule = etra_names_fantasy")
            lines.append(f"\t\t\truler ?= {{ culture.language = language:{lang_id} }}")
            lines.append("\t\t}")
            lines.append("\t}")

        # 2. Native fallback (fantasy mode, ruler language not in translations)
        if native:
            native_key = f"{tag}_{lang_short(native)}"
            if native in translations:
                lines.append("\ttext = {")
                lines.append(f"\t\tlocalization_key = {native_key}")
                lines.append("\t\ttrigger = {")
                lines.append("\t\t\thas_game_rule = etra_names_fantasy")
                lines.append("\t\t}")
                lines.append("\t}")
            else:
                # Native language has no translation — use first available
                first_lang = next(iter(translations))
                fallback_key = f"{tag}_{lang_short(first_lang)}"
                lines.append("\ttext = {")
                lines.append(f"\t\tlocalization_key = {fallback_key}")
                lines.append("\t\ttrigger = {")
                lines.append("\t\t\thas_game_rule = etra_names_fantasy")
                lines.append("\t\t}")
                lines.append("\t}")

        # 3. English fallback
        lines.append("\ttext = {")
        lines.append(f"\t\tlocalization_key = {tag}")
        lines.append("\t\tfallback = yes")
        lines.append("\t}")

        lines.append("}")

        # ADJ shortcut: reuses same function with _ADJ suffix on all loc keys
        if data.get("adjectives"):
            lines.append(f'{tag}_ADJ = {{ parent = {tag} suffix = "_ADJ" }}')

        lines.append("")

    return "\n".join(lines) + "\n"


