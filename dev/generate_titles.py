def load_config(path: Path) -> dict:
    """Load and validate title translation config."""
    with open(path, encoding="utf-8") as f:
        cfg = yaml.safe_load(f)

    if not cfg.get("titles"):
        cfg["titles"] = {}

    # Validate translations
    for tag, data in cfg.get("titles", {}).items():
        if not isinstance(data, dict):
            print(f"WARNING: Invalid entry for {tag}, skipping", file=sys.stderr)
            continue
        if "translations" not in data or not data["translations"]:
            print(f"WARNING: {tag} has no translations", file=sys.stderr)

    return cfg


