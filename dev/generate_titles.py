def generate_game_rule(cfg: dict) -> str:
    """Generate game_rules .txt content."""
    gr = cfg.get("game_rule", {})
    key = gr.get("key", "etra_title_names")
    default = gr.get("default", "fantasy")
    default_setting = f"etra_names_{default}"

