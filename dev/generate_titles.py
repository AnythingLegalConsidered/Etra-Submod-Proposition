def main():
    parser = argparse.ArgumentParser(
        description="Generate EU5/Etra fantasy title translations.",
    )
    parser.add_argument(
        "config",
        nargs="?",
        default=None,
        help="Path to title translations YAML config",
    )
    parser.add_argument(
        "--list",
        action="store_true",
        help="List all titles and their translation status",
    )
    parser.add_argument(
        "--audit",
        action="store_true",
        help="Find English country names not yet in config",
    )
    parser.add_argument(
        "--output", "-o",
        default=None,
        help="Output directory (default: overrides/)",
    )
    args = parser.parse_args()

    # Find config
    config_path = (
        Path(args.config)
        if args.config
        else SCRIPT_DIR / "titles" / "config.yaml"
    )
    if not config_path.is_file():
        print(f"ERROR: Config not found: {config_path}", file=sys.stderr)
        sys.exit(1)

    cfg = load_config(config_path)

    if args.list:
        list_titles(cfg)
        return

    if args.audit:
        audit_titles(cfg)
        return

    # Output to overrides/ (picked up by generate_mod.py --all)
    out_dir = Path(args.output) if args.output else SCRIPT_DIR / "overrides"

    game_rule_dir = out_dir / "main_menu" / "common" / "game_rules"
    custom_loc_dir = out_dir / "in_game" / "common" / "customizable_localization"
    loc_dir = out_dir / "main_menu" / "localization" / "english"

    for d in (game_rule_dir, custom_loc_dir, loc_dir):
        d.mkdir(parents=True, exist_ok=True)

    titles = cfg.get("titles", {})
    has_titles = any(
        isinstance(d, dict) and d.get("translations")
        for d in titles.values()
    )

    # 1. Game rule (always generated — toggle is useful even with 0 translations)
    game_rule_content = generate_game_rule(cfg)
    game_rule_path = game_rule_dir / "etra_title_names.txt"
    with open(game_rule_path, "w", encoding="utf-8-sig") as f:
        f.write(game_rule_content)
    print(f"  Game rule     -> {game_rule_path}")

    # 2. Localization (always — contains game rule labels + ruler titles)
    loc_content = generate_localization(cfg)
    ruler_loc = generate_ruler_titles_loc(cfg)
    if ruler_loc:
        loc_content = loc_content.rstrip("\n") + "\n" + ruler_loc + "\n"
    loc_path = loc_dir / "etra_title_names_l_english.yml"
    with open(loc_path, "w", encoding="utf-8-sig") as f:
        f.write(loc_content)
    print(f"  Localization  -> {loc_path}")

    # 3. Customizable localization — country names (only if there are translations)
    if has_titles:
        custom_loc_content = generate_custom_loc(cfg)
        custom_loc_path = custom_loc_dir / "etra_title_names.txt"
        with open(custom_loc_path, "w", encoding="utf-8-sig") as f:
            f.write(custom_loc_content)
        print(f"  Custom loc    -> {custom_loc_path}")

    # 4. Country name construction override (forces $NAME$ for our titles)
    if has_titles:
        construction_content = generate_name_construction(cfg)
        if construction_content:
            construction_path = custom_loc_dir / "country_name_construction.txt"
            with open(construction_path, "w", encoding="utf-8-sig") as f:
                f.write(construction_content)
            print(f"  Construction  -> {construction_path}")

    # 5. Customizable localization — ruler titles
    ruler_titles = cfg.get("ruler_titles", {})
    ruler_custom_loc = generate_ruler_titles_custom_loc(cfg)
    if ruler_custom_loc:
        ruler_path = custom_loc_dir / "00_etra_ruler_titles.txt"
        with open(ruler_path, "w", encoding="utf-8-sig") as f:
            f.write(ruler_custom_loc)
        print(f"  Ruler titles  -> {ruler_path}")

    # Stats
    n_titles = sum(
        1 for d in titles.values()
        if isinstance(d, dict) and d.get("translations")
    )
    n_translations = sum(
        len(d.get("translations", {}))
        for d in titles.values()
        if isinstance(d, dict)
    )
    # Ruler title stats
    n_ruler_dialects = sum(len(dl) for dl in ruler_titles.values())
    n_ruler_entries = sum(
        sum(1 for r in RANKS if d.get(r))
        for dl in ruler_titles.values()
        for d in dl
    )
    print(f"\nDone. {n_titles} country names, {n_translations} translations.")
    print(f"      {n_ruler_dialects} ruler dialects, {n_ruler_entries} rank entries.")
    if not has_titles and not ruler_titles:
        print("No translations yet — add entries to titles/config.yaml")
        print("Run: python generate_titles.py --audit  (find English names to translate)")


if __name__ == "__main__":
    main()
