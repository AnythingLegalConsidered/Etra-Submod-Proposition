# Generator: Ruler Titles (Customizable Localization)
# ---------------------------------------------------------------------------

RANKS = ["empire", "kingdom", "duchy", "county"]
RANK_CHECKS = {
    "empire": "country_rank_is_empire",
    "kingdom": "country_rank_is_kingdom",
    "duchy": "country_rank_is_duchy",
    "county": "country_rank_is_county",
}
RANK_DISPLAY = {
    "empire": "Empire",
    "kingdom": "Kingdom",
    "duchy": "Duchy",
    "county": "County",
}


def ruler_loc_key(dialect_name: str, rank: str) -> str:
    """Build loc key for a ruler title entry."""
    slug = dialect_name.lower().replace(" ", "_")
    return f"etra_rank_{rank}_{slug}"


def generate_ruler_titles_custom_loc(cfg: dict) -> str:
    """Generate country_flavor entries for fantasy ruler titles.

    Creates a country_flavor block with per-dialect triggers.
    Specific dialect entries come first, fallback (language-level) entries
    come last so first-match gives the most specific result.
    """
    ruler_titles = cfg.get("ruler_titles", {})
    if not ruler_titles:
        return ""

    lines = [
        "# Etra: Names & Cultures — Fantasy ruler titles (Monarchy)",
        "# Generated by generate_titles.py",
        "#",
        "# When game rule = Fantasy Languages and government = Monarchy,",
        "# ruler titles (Emperor/King/Duke/Count) display in the",
        "# ruler's culture/dialect language.",
        "#",
        "# Entries are added to country_flavor and evaluated before",
        "# the base game's default entries (file loads first via 00_ prefix).",
        "",
        "country_flavor = {",
        "\ttype = country",
        "",
    ]

    # Collect entries separated by specificity
    specific = []
    fallback = []

    for lang_name, dialects_list in ruler_titles.items():
        for d in dialects_list:
            is_fb = d.get("fallback", False)
            target = fallback if is_fb else specific
            trigger_type = d.get("trigger", "language")
            trigger_id = d["id"]
            dialect_name = d["dialect"]
            also_matches = d.get("also_matches", [])

            for rank in RANKS:
                titles = d.get(rank)
                if not titles:
                    continue

                loc_key = ruler_loc_key(dialect_name, rank)

                # Main entry
                target.append({
                    "loc_key": loc_key,
                    "rank": rank,
                    "trigger_id": trigger_id,
                    "trigger_type": trigger_type,
                    "dialect": dialect_name,
                    "lang": lang_name,
                })

                # Extra entries for also_matches (same loc key, different trigger)
                for extra_id in also_matches:
                    target.append({
                        "loc_key": loc_key,
                        "rank": rank,
                        "trigger_id": extra_id,
                        "trigger_type": "language",
                        "dialect": dialect_name,
                        "lang": lang_name,
                        "extra": True,
                    })

    def write_entry(entry):
        rank_check = RANK_CHECKS[entry["rank"]]
        comment = f"\t# {entry['lang'].title()} — {entry['dialect']}"
        if entry.get("extra"):
            comment += f" (also: {entry['trigger_id']})"
        lines.append(comment)
        lines.append("\ttext = {")
        lines.append(f"\t\tlocalization_key = {entry['loc_key']}")
        lines.append("\t\ttrigger = {")
        lines.append("\t\t\thas_game_rule = etra_names_fantasy")
        lines.append(f"\t\t\t{rank_check} = yes")
        lines.append("\t\t\tgovernment_type = government_type:monarchy")
        if entry["trigger_type"] == "culture":
            lines.append(
                f"\t\t\truler ?= {{ culture = culture:{entry['trigger_id']} }}"
            )
        else:
            lines.append(
                f"\t\t\truler ?= {{ culture.language = language:{entry['trigger_id']} }}"
            )
        lines.append("\t\t}")
        lines.append("\t}")

    # Write specific entries first (most specific triggers)
    if specific:
        lines.append("\t# === SPECIFIC DIALECT ENTRIES ===")
        lines.append("")
        current_lang = None
        for entry in specific:
            if entry["lang"] != current_lang:
                if current_lang is not None:
                    lines.append("")
                current_lang = entry["lang"]
                lines.append(f"\t# --- {current_lang.upper()} ---")
            write_entry(entry)
        lines.append("")

    # Write fallback entries (language-level, less specific)
    if fallback:
        lines.append("\t# === FALLBACK ENTRIES (language-level) ===")
        lines.append("")
        for entry in fallback:
            write_entry(entry)
        lines.append("")

    lines.append("}")
    return "\n".join(lines) + "\n"


